const Mongoose = require("mongoose");
 
const AdminSchema = Mongoose.Schema({
    nom: {
        type: String,
        required: true
    },
    prenom: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    login: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});

const Admin = Mongoose.model('Admin', AdminSchema, 'Admin');

module.exports= Admin;